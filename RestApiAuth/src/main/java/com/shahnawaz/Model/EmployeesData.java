package com.shahnawaz.Model;

public class EmployeesData {
	
	private int id;
	private String name;
	private int rewardPoints;
	private double actualWorkHour;
	private double totalWorkHour;
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getRewardPoints() {
		return rewardPoints;
	}
	public void setRewardPoints(int rewardPoints) {
		this.rewardPoints = rewardPoints;
	}
	public double getActualWorkHour() {
		return actualWorkHour;
	}
	public void setActualWorkHour(double actualWorkHour) {
		this.actualWorkHour = actualWorkHour;
	}
	public double getTotalWorkHour() {
		return totalWorkHour;
	}
	public void setTotalWorkHour(double totalWorkHour) {
		this.totalWorkHour = totalWorkHour;
	}
	
	
	

}
